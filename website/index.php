<div id="right"> <h2>What is Simon?</h2>

    <p>Simon is a C++ library for physically plausible dynamics simulations. It
    provides functions to compute the motion of rigid bodies and particles. Its
    features include collision resolution with resting contact, friction,
    articulated joints and spring connections.<br />

    The "simon" library's emphasis is on interactive and real-time applications. 
    Furthermore it provides an easy-to-use interface. </p>

    <p>Most of the library's implementations are based on state of the art
    computer graphics research. Simon e.g. draws upon results published by 
    <a href="http://www.pixar.com/companyinfo/research/deb/">David Baraff</a> and
    <a href="http://graphics.stanford.edu/~fedkiw/">Ron Fedkiw</a>.</p> </div>

